namespace WpfApp1
{
    /// <summary>
    /// Interaction logic for MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void Button_Click(object sender, RoutedEventArgs e)
        {
            var button = sender as Button; // this avoids an invalid cast exception. If the cast is invalid it returns null

            if (button != null)
            {
                // downcast was successful
                MessageBox.Show("Button height: " + button.ActualHeight.ToString());
            }
            MessageBox.Show("Hello World!");
        }
    }
}
